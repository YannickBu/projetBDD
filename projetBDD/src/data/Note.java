package data;

public class Note {

	private CC cc;
	private Etudiant etu;
	private Integer note;
	
	public Note() {
		
	}

	public CC getCc() {
		return cc;
	}

	public void setCc(CC cc) {
		this.cc = cc;
	}

	public Etudiant getEtu() {
		return etu;
	}

	public void setEtu(Etudiant etu) {
		this.etu = etu;
	}

	public Integer getNote() {
		return note;
	}

	public void setNote(Integer note) {
		this.note = note;
	}

	
}
